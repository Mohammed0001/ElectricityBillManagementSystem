    public void getCustomers(){
        customers = Customer.getCustomers();
        for (Customer customer : customers) {
          jComboBox1.addItem(String.valueOf(customer.getId()) +" " + String.valueOf(customer.getName()));
        }
    }
    public void viewBills(int id){
        if (user instanceof Customer) {
           bills = Bill.viewBills(user.getId());
        }else{
            bills = Bill.viewBills(id);
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for(Bill bill : bills){
            model.addRow(new Object[]{bill.getId() ,bill.getAmount(),bill.getDiscount()});
        }
    } 
    
